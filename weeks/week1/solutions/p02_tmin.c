#include <stdint.h>

/*
 * 题目: 返回 int32 的最小值 Tmin。
 * 思路: 用无符号 1 左移 31 位，构造位模式 1000...0000。
 *
 * 关键点:
 *   1u << 31 得到 0x80000000（最高位1，其余全0）
 *   再转成 int 后按补码解释，即 Tmin。
 *
 * 为什么这是最小值:
 *   32 位补码范围是 [-2^31, 2^31-1]，
 *   所以 0x80000000 对应 -2^31 = -2147483648。
 */
int tmin(void) {
  return (int)(1u << 31);
}
