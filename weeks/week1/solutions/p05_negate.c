#include <stdint.h>

/*
 * 题目: 计算 -x。
 * 思路: 在补码里，负数可由“按位取反再加1”得到。
 * 推导:
 *   x + ~x = -1
 *   x + (~x + 1) = 0
 *   因此 ~x + 1 是 x 的加法逆元，即 -x。
 * 例子:
 *   x=5(....0101) -> ~x=....1010 -> ~x+1=....1011(即 -5)
 * 特殊值:
 *   x=INT_MIN 时结果仍是 INT_MIN（补码范围不对称，+2147483648 不可表示）。
 */
int negate(int x) {
  return ~x + 1;
}
